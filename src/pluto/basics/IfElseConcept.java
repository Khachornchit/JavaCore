public class IfElseConcept {

	public static void main(String[] args) {
		int a = 30;
		int b = 40;
		int c = 90;
		int d = 48;

		if (b > a & c > d) {
			System.out.println("b>a & c> d");
		} else {
			System.out.println("b<a | c<d");
		}
	}

}