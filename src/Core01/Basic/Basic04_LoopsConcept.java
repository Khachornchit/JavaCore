package Core01.Basic;

public class Basic04_LoopsConcept {

	public static void main(String[] args) {
		int i = 1;

		System.out.println("While loop");
		while (i <= 10) {
			System.out.println("i = " + i++);
		}
		
		System.out.println("For loop");
		for (int j = 0; j < 10; j++) {
			System.out.println("i = " + j);
		}
	}

}